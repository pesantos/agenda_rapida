    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(valido()){
            Compromisso comp = new Compromisso();
            comp.setAviso(Integer.parseInt(campoDia.getText().trim()));
            comp.setDescricao(campoDescricao.getText().trim());
            comp.setDia(campoData.getDate());
            comp.setAtivo("sim");
            if(logado.getCompromissos()==null)logado.setCompromissos(new LinkedList<Compromisso>());
            logado.getCompromissos().add(comp);
            udao.atualizar();
            pai.atualizarTabela();
            JOptionPane.showMessageDialog(this, "Compromisso cadastrado com sucesso!!");
            carregarTabela();
            limparFormulario();
            
            
        }
    }//GEN-LAST:event_jButton1ActionPerformed



