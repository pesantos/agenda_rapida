    private boolean valido(){
        if(campoDescricao.getText().equals(""))return false;
        if(campoData.getDate()==null)return false;
        if(campoDia.getText().equals(""))return false;
        return true;
    }
    
    private final Principal pai;
    private final Usuario logado;
    private final AnnotationResolver resolver = new AnnotationResolver(Compromisso.class);
    private final ObjectTableModel modelo = new ObjectTableModel(resolver,"dia,descricao,aviso");
    private final CompromissoDAO codao = new CompromissoDAO();
    private final UsuarioDAO udao = new UsuarioDAO();
    
    
    private void inicializar(){
        this.setLocationRelativeTo(null);
        this.setTitle("Cadastro de Compromissos - "+logado.getNome());
        this.setVisible(true);
        
        
        carregarTabela();
    }
    
    private void carregarTabela(){
        modelo.clear();
        if(logado.getCompromissos()!=null && !logado.getCompromissos().isEmpty()){
            Collections.sort(logado.getCompromissos());
            modelo.setData(logado.getCompromissos());
        }
    }
    
    private void limparFormulario(){
        campoData.setDate(null);
        campoDescricao.setText("");
        campoDia.setText("");
        campoDescricao.requestFocus();
    }

